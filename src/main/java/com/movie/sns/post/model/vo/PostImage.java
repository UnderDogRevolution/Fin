package com.movie.sns.post.model.vo;

public class PostImage {
	private int postImageNo;
	private String postImagePath;
	private String postImageName;
	private String postImageDate;
	private String postImageOriginal;
	private int postImageLevel;
	private int postNo;
	public int getPostImageNo() {
		return postImageNo;
	}
	public void setPostImageNo(int postImageNo) {
		this.postImageNo = postImageNo;
	}
	public String getPostImagePath() {
		return postImagePath;
	}
	public void setPostImagePath(String postImagePath) {
		this.postImagePath = postImagePath;
	}
	public String getPostImageName() {
		return postImageName;
	}
	public void setPostImageName(String postImageName) {
		this.postImageName = postImageName;
	}
	public String getPostImageDate() {
		return postImageDate;
	}
	public void setPostImageDate(String postImageDate) {
		this.postImageDate = postImageDate;
	}
	public String getPostImageOriginal() {
		return postImageOriginal;
	}
	public void setPostImageOriginal(String postImageOriginal) {
		this.postImageOriginal = postImageOriginal;
	}
	
	public int getPostNo() {
		return postNo;
	}
	public void setPostNo(int postNo) {
		this.postNo = postNo;
	}
	public int getPostImageLevel() {
		return postImageLevel;
	}
	public void setPostImageLevel(int postImageLevel) {
		this.postImageLevel = postImageLevel;
	}
	
	
	
	
}
